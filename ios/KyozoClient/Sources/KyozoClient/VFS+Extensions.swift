            Task {
                do {
                    let content = try await self.vfs.readContent(
                        teamId: teamId,
                        workspaceId: workspaceId,
                        path: path
                    )
                    promise(.success(content))
                } catch {
                    promise(.failure(error))
                }
            }
        }
        .eraseToAnyPublisher()
    }
}
